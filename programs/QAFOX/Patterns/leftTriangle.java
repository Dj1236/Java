package programs.QAFOX.Patterns;

public class leftTriangle {
    public static void main(String[] args) {
        for(int i=5;i<=5;i--){
            for(int j =1;j<=i;j++){
                System.out.print("*");
            }
            System.out.print(" ");

        }
        System.out.println("");
    }
    
}
