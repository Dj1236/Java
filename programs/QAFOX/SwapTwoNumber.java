package programs.QAFOX;

public class SwapTwoNumber {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int temp;
        System.out.println("before swaping the value is a:"+a+"b:"+b);
        temp = a;
        a= b;
        b = temp;
        System.out.println("after swaping the value is a:"+a+"b:"+b);
    }
    
}
